package constraints;

public class IncompatibleRealSetError extends Error {
	
	private static final long serialVersionUID = -9037025996531383607L;

	public IncompatibleRealSetError() {
		super();
	}

	public IncompatibleRealSetError(String message, Throwable cause) {
		super(message, cause);
	}

	public IncompatibleRealSetError(String message) {
		super(message);
	}

	public IncompatibleRealSetError(Throwable cause) {
		super(cause);
	}

}
