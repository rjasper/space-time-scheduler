package scheduler.constraints;

public class VariableIncompleteError extends Error {
	
	private static final long serialVersionUID = 8426777444057455171L;

	public VariableIncompleteError() {
		super();
	}

	public VariableIncompleteError(String message, Throwable cause) {
		super(message, cause);
	}

	public VariableIncompleteError(String message) {
		super(message);
	}

	public VariableIncompleteError(Throwable cause) {
		super(cause);
	}

}
